    private string GenerateModelInit(ModelGenConfig modelGenConfig)
    {
        StringBuilder stringBuilder = new StringBuilder();
        stringBuilder.Append($@"
    protected override void AutoGenInit()
    {{");
        
        foreach (AttributeConfig attributeConfig in modelGenConfig.RelevantAttributes)
        {
            if (attributeConfig is ObservableAttributeConfig observable && observable.FieldIsTypeModel)
            {
                if (observable.IsList)
                {
                    stringBuilder.Append($@"
        if ({observable.FieldName} != null)
        {{
            foreach({observable.ListType} element in {observable.FieldName})
            {{
                {observable.PropName}_BindElementOnChanged(element);
            }}
        }}");
                }
                else
                {
                    stringBuilder.Append($@"
        if ({observable.FieldName} != null)
        {{
            {observable.FieldName}.OnChanged += m_onChanged;
        }}");
                }
            }
            else if (attributeConfig is PassthroughAttributeConfig passthrough)
            {
                if (passthrough.IsList)
                {
                }
                else
                {
                }
            }
        }
        
        stringBuilder.Append($@"
    }}
");   
        
        return stringBuilder.ToString();
    }

    private string GenerateModelOnChangedAction(ModelGenConfig modelGenConfig)
    {
        return $@"
    protected Action<BaseModel> m_onChanged;
    private Action m_onChanged_OnDelegatesChanged;
    public override event Action<BaseModel> OnChanged
    {{
        add
        {{
            m_onChanged += value;
            m_onChanged_OnDelegatesChanged?.Invoke();
        }}
        remove
        {{
            m_onChanged -= value;
            m_onChanged_OnDelegatesChanged?.Invoke();
        }}
    }}
";
    }

    private string GenerateSafeEqualityComparerFunction()
    {
        return $@"
    private bool SafeEquals<T>(T a, T b)
    {{
        return EqualityComparer<T>.Default.Equals(a, b);
    }}";
    }
