namespace MyMVVMSourceGenerators;

[Generator]
public class MyMVVMSourceGenerator : IIncrementalGenerator
{
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        var provider = context.SyntaxProvider.CreateSyntaxProvider(
                (s, _) => s is ClassDeclarationSyntax,
                (ctx, _) => new ModelGenConfig(ctx))
            .Where(m => m.ShouldGen)
            .Collect();
        
        context.RegisterSourceOutput(provider, GenerateCode);
    }

    private void GenerateCode(SourceProductionContext context, ImmutableArray<ModelGenConfig> modelGenConfigs)
    {
        foreach (ModelGenConfig modelGenConfig in modelGenConfigs)
        {
            var sourceBuilder = new StringBuilder();
            
            sourceBuilder.Append($@"// AutoGenerated code by MyMVVM.
{FormatFieldNamespaces(modelGenConfig)}{FormatNamespaceString(modelGenConfig)}
public partial class {modelGenConfig.ClassName}
{{");
            foreach (var attrConfig in modelGenConfig.RelevantAttributes)
            {
                if (attrConfig is ObservableAttributeConfig observableAttr)
                {
                    if (observableAttr.IsList)
                    {
                        if (observableAttr.FieldIsTypeModel)
                        {
                            sourceBuilder.Append(GenerateModelObservableList(observableAttr));
                        }
                        else
                        {
                            sourceBuilder.Append(GenerateObservableList(observableAttr));
                        }
                    }
                    else
                    {
                        if (observableAttr.FieldIsTypeModel)
                        {
                            sourceBuilder.Append(GenerateModelObservable(observableAttr));
                        }
                        else
                        {
                            sourceBuilder.Append(GenerateObservable(observableAttr));
                        }
                    }
                }
                else if (attrConfig is PassthroughAttributeConfig passthroughAttr)
                {
                    if (passthroughAttr.IsList)
                    {
                        sourceBuilder.Append(GeneratePassthroughList(passthroughAttr));
                    }
                    else
                    {
                        sourceBuilder.Append(GeneratePassthrough(passthroughAttr));
                    }
                }
            }

            sourceBuilder.Append(GenerateModelInit(modelGenConfig));
            sourceBuilder.Append(GenerateModelOnChangedAction(modelGenConfig));
            sourceBuilder.Append(GenerateSafeEqualityComparerFunction());
            sourceBuilder.Append($@"{FormatNamespaceStringEnder(modelGenConfig)}
}}");
            
            context.AddSource($"{modelGenConfig.ClassName}.g.cs", SourceText.From(sourceBuilder.ToString(), Encoding.UTF8));
        }
    }
    
    private string FormatNamespaceString(ModelGenConfig modelGenConfig)
    {
        return string.IsNullOrEmpty(modelGenConfig.Namespace) ? "" : @$"
namespace {modelGenConfig.Namespace}
{{";
    }

    private string FormatNamespaceStringEnder(ModelGenConfig modelGenConfig)
    {
        return string.IsNullOrEmpty(modelGenConfig.Namespace) ? "" : "}";
    }

    private string FormatParentClassString(ModelGenConfig modelGenConfig)
    {
        return string.IsNullOrEmpty(modelGenConfig.ParentClassName) ? "" : $" : {modelGenConfig.ParentClassName}";
    }

    private string FormatFieldNamespaces(ModelGenConfig modelGenConfig)
    {
        HashSet<string> namespaces = new();
        namespaces.Add("MyMVVM");
        namespaces.Add("System");
        namespaces.Add("System.Collections.Generic");
        if (modelGenConfig.RelevantAttributes.Any(x => x.IsList))
        {
            namespaces.Add("System.Collections.ObjectModel");
        }
        foreach(AttributeConfig attrConfig in modelGenConfig.RelevantAttributes)
        {
            if (!string.IsNullOrEmpty(attrConfig.TypeNamespace))
            {
                namespaces.Add(attrConfig.TypeNamespace);
            }
        }

        namespaces.Remove("<global namespace>");
        
        StringBuilder stringBuilder = new();
        foreach(string namespaceStr in namespaces)
        {
            stringBuilder.Append($"using {namespaceStr};\n");
        }
        return stringBuilder.ToString();
    }

    private string GenerateModelObservable(ObservableAttributeConfig observable)
    {
        return $@"
    // === Generated Observable for {observable.FieldName} ===
    public {observable.FieldType} {observable.PropName} => {observable.FieldName};
    
    public void {observable.PropName}_Set({observable.FieldType} value, bool notify = true)
    {{
        if (SafeEquals({observable.FieldName}, value))
        {{
            return;
        }}

        if ({observable.FieldName} != null)
        {{
            {observable.FieldName}.OnChanged -= m_onChanged;
        }}

        {observable.FieldName} = value;
        {observable.FieldName}.OnChanged += m_onChanged;

        if (notify)
        {{
            m_onChanged?.Invoke(this);
        }}
    }}
    // ====================
";
    }

    private string GenerateOnChangedAction(string callbackName, string fieldType)
    {
        if (!string.IsNullOrEmpty(fieldType))
        {
            fieldType = $"<{fieldType}>";
        }
        
        return $@"private Action{fieldType} m_{callbackName};
    private Action m_{callbackName}_OnDelegatesChanged;
    public event Action{fieldType} {callbackName}
    {{
        add
        {{
            m_{callbackName} += value;
            m_{callbackName}_OnDelegatesChanged?.Invoke();
        }}
        remove
        {{
            m_{callbackName} -= value;
            m_{callbackName}_OnDelegatesChanged?.Invoke();
        }}
    }}";
    }
    
    private string GenerateObservable(ObservableAttributeConfig observable)
    { 
        return $@"
    // === Generated Observable for {observable.FieldName} ===
    public {observable.FieldType} {observable.PropName} => {observable.FieldName};

    {GenerateOnChangedAction($"{observable.PropName}_OnChanged", observable.FieldType)}
    
    public void {observable.PropName}_Set({observable.FieldType} value, bool notify = true)
    {{
        if (SafeEquals({observable.FieldName}, value))
        {{
            return;
        }}

        {observable.FieldName} = value;
        if (notify)
        {{
            {observable.PropName}_TriggerOnChanged();
        }}
    }}

    public void {observable.PropName}_TriggerOnChanged()
    {{
        m_{observable.PropName}_OnChanged?.Invoke({observable.FieldName});
        m_onChanged?.Invoke(this);
    }}
    // ====================
";
    }

    private string GenerateModelObservableList(ObservableAttributeConfig observable)
    {
        return $@"
